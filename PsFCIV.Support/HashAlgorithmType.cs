namespace PsFCIV.Support {
    /// <summary>
    /// Represents enumeration of hash algorithms.
    /// </summary>
    public enum HashAlgorithmType {
        /// <summary>
        /// 128-bit MD5
        /// </summary>
        MD5,
        /// <summary>
        /// 160-bit SHA1
        /// </summary>
        SHA1,
        /// <summary>
        /// 256-bit SHA256
        /// </summary>
        SHA256,
        /// <summary>
        /// 384-bit SHA384
        /// </summary>
        SHA384,
        /// <summary>
        /// 512-bit SHA512
        /// </summary>
        SHA512
    }
}