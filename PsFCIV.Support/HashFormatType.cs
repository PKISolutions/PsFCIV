namespace PsFCIV.Support {
    /// <summary>
    /// Represents binary data text format.
    /// </summary>
    public enum HashFormatType {
        /// <summary>
        /// Binary data is represented as raw hex string.
        /// </summary>
        Hex,
        /// <summary>
        /// Binary data is represented as Base64-encoded string.
        /// </summary>
        Base64
    }
}